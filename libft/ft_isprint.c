#include "libft.h"

int	ft_isprint(int c)
{
	if (c >= 32 && c <= 126)
		return (1);
	return (0);
}

int	ft_isprint_nospace(int c)
{
	if (c >= 33 && c <= 126)
		return (1);
	return (0);
}
